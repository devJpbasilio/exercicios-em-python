
"""
Encontrando o maior e o menor valor de uma tupla
Dada a tupla: numeros = (45, 3, 89, 21, 67, 34, 10)

1 - Encontre o maior número da tupla.
2 - Encontre o menor número da tupla.
"""

numeros = (45, 3, 89, 21, 67, 34, 10)
print(numeros, "\n")

print(f"O Maior número: {max(numeros)}")
print(f"O menor menor: {min(numeros)}")